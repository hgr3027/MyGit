package com.tutk.P2PCam264.DeviceOnCloud;



public class OnCloudDeviceInfo {

	public String NickName;
	public String UID;
	public String View_Account;
	public String View_Password;
	public String Place;
	public String Detail;
	public String Location;
	
	

	public OnCloudDeviceInfo(String nickname, String uid, String view_acc, String view_pwd, String place,String detail,
			String location) {

		NickName = nickname;
		UID = uid;
		View_Account = view_acc;
		View_Password = view_pwd;
		Place = place;
		Detail = detail;
		Location = location;
		
	}
}
